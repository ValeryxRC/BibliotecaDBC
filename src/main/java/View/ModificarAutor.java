    private void modificarAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarAActionPerformed
        //Encuentro el libro selcc
        String credencial = (String) autores.getSelectedItem();
        try {
            Autor auAnt = iAcont.readAutor(credencial);
            String nombre = nombreA.getText();
            String ape1 = p1A.getText();
            String ape2 = p2A.getText();
            String lugar = (String) lugNac.getSelectedItem();
            Autor auAct = new Autor(nombre, ape1, ape2, lugar);
            iAcont.updateAutors(auAnt, auAct);
            JOptionPane.showMessageDialog(this, "Autor modificado", "Modicar Autor", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } catch (DAO_Excep | AutorException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error  Modificar Autor", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_modificarAActionPerformed

