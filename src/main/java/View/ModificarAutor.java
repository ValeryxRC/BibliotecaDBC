    private void autoresItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_autoresItemStateChanged
        // Encuentro el autor seleccionado en el jframe y muestro sus datos
        String credencial = (String) autores.getSelectedItem();
        try {
            Autor au = iAcont.readAutor(credencial);
            nombreA.setText(au.getNombre());
            p1A.setText(au.getApe1());
            p2A.setText(au.getApe2());
            lugNac.setSelectedItem(au.getLugarNac());
        } catch (DAO_Excep | AutorException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error de lectura", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_autoresItemStateChanged

