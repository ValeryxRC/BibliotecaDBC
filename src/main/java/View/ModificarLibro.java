    private void LibroSItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_LibroSItemStateChanged
        // Recolecto el libro seleccionado y mustro sus datos atravez del jframe
        String libro = (String) LibroS.getSelectedItem();
        Libro lib = null;
        try {
            String[] splitLib = libro.split("-");
            int isbnLib = Integer.parseInt(splitLib[0]);
            lib = iLibcon.readLibro(isbnLib);
            isbn.setText(String.valueOf(lib.getIsbn()));
            tituloM.setText(lib.getTitulo());
            generoL.setSelectedItem(lib.getGenero());
            autorL.setSelectedItem(lib.getAutor().getNombre() + " " + lib.getAutor().getApe1() + " " + lib.getAutor().getApe2());
            descripL.setText(lib.getDescript());
        } catch (LibroException | DAO_Excep ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_LibroSItemStateChanged

