    public ModificarLibro() {
        initComponents();
         //Rellena el combo box con los autores disponibles en la biblioteca
        List<Autor> aus;
        try {
            aus = iAcont.readAllAutors();
            for (Autor au : aus) {
                autorL.addItem(au.getNombre() + " " + au.getApe1() + " " + au.getApe2());
            }
        } catch (DAO_Excep | AutorException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error de lectura autores", JOptionPane.ERROR_MESSAGE);
        }
        //Relleno en el combo box los libros disponibles a modificar
        List<Libro> libs;
        Libro lib = null;
        try {
            libs = iLibcon.readAllLibro();
            for (Libro li : libs) {
                System.out.println(li);
                LibroS.addItem(li.getIsbn() + "-" + li.getTitulo());
            }
            String libro = (String) LibroS.getSelectedItem();
            String[] splitLib = libro.split("-");
            int isbnLib = Integer.parseInt(splitLib[0]);
            lib = iLibcon.readLibro(isbnLib);
            autorL.setSelectedItem(lib.getAutor().getNombre() + " " + lib.getAutor().getApe1() + " " + lib.getAutor().getApe2());
        } catch (LibroException | DAO_Excep ex) {
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error de lectura de Libros", JOptionPane.ERROR_MESSAGE);
        }
    }

